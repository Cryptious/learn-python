# Biasanya digunakan untuk sebuah kondisi(if statemen) atau perulangan
# Membandingkan 2 variabel dan menghasilkan TRUE atau FALSE

x = 12.0
y = 5.0

print (" x == y ", x == y) # sama dengan, tanda "=" hanya digunakan untuk mendeklarasikan variabel
print (" x != y ", x != y) # tidak sama dengan
print (" x > y ", x > y) # Lebih besar dari
print (" x >= y ", x >= y) # Lebih besar sama dengan
print (" x < y ", x < y) # Lebih Kecil
print (" x <= y ", x <= y) # Lebih Kecil sama dengan
