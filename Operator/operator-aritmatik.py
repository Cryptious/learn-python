x = 12
y = 5

# Penambahan
result = x + y
print (" x + y ", result)

# Pengurangan
result = x - y
print (" x - y ", result)

# Perkalian
result = x * y
print (" x * y ", result)

# Pembagian
result = x / y
print (" x / y ", result)

# Modulus/ Sisa dari Pembagian
result = x % y
print (" x % y ", result)

# Pangkat
result = x ** y
print (" x ** y ", result)

# Pembagian Bulat
result = x // y
print (" x // y ", result)
