# Operator assignment :

x = 18
x += 8 # +=, melakukan operasi penambahan terhadap variabel itu sendiri
print (" x += 8 ",  x)

x = 18
x -= 10 # -=, melakukan operasi pengurangan terhadap variabel itu sendiri
print (" x -= 8 ",  x)

x = 18
x *= 8 # *=, melakukan operasi perkalian terhadap variabel itu sendiri
print (" x *= 8 ",  x)

x = 18
x /= 8 # /=, melakukan operasi pembagian terhadap variabel itu sendiri
print (" x /= 8 ",  x)

x = 18
x //= 8 # //=, melakukan operasi pembagian bulat terhadap variabel itu sendiri
print (" x //= 8 ",  x)

x = 18
x %= 8 # %=, melakukan operasi modulus terhadap variabel itu sendiri
print (" x %= 8 ",  x)

x = 5
x **= 3 # **=, melakukan operasi pangkat terhadap variabel itu sendiri
print (" x **= 3 ",  x)
