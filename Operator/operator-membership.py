# Operator keanggotaan digunakan untuk memeriksa apakah suatu data terdapat dalam list, tuple, atau dictionary
angka = [1 , 2, 3, 4 ,5]

print (angka)
print ("1 in angka: ", 1 in angka) # in, apakah suatu elemen ada di dalam list / tuple / dictionary
print ("10 not in angka: ", 10 not in angka) # not in, apakah suatu elemen tidak ada di dalam list / tuple / dictionar
